    public void restart(Window win, long spd, int alg, int num) {
        win.invalidate();
        win.setVisible(false);
        win.removeAll();
        getContentPane().remove(win);
        win = null;
        win2 = new Window(width, height, spd, alg, this,num);
        win2.startThread();
        getContentPane().add(win2);
        invalidate();
        validate();
    }
   
